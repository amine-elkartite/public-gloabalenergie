(function(){
  function $(sel, root){ return (root || document).querySelector(sel); }
  function $all(sel, root){ return Array.prototype.slice.call((root || document).querySelectorAll(sel)); }
  var ALLOWED_BRANDS = ['Carrier', 'Fitco', 'Midea', 'Ingelec'];
  var BRAND_LOGOS = {
    'Carrier': './assets/brand-carrier.png',
    'Fitco': './assets/brand-fitco.png',
    'Midea': './assets/brand-midea.png',
    'Ingelec': './assets/brand-ingelec.png'
  };
  function normalize(txt){
    return (txt || '')
      .toString()
      .toLowerCase()
      .normalize('NFD')
      .replace(/[\u0300-\u036f]/g, '')
      .replace(/[’']/g, ' ')
      .replace(/[^a-z0-9]+/g, ' ')
      .trim();
  }
  function escapeAttr(txt){ return (txt || '').replace(/"/g, '&quot;'); }
  function uniqueValues(key){
    if(key === 'brand') {
      return ALLOWED_BRANDS.slice();
    }
    return window.GE_PRODUCTS
      .map(function(p){ return p[key]; })
      .filter(function(v, i, arr){ return v && arr.indexOf(v) === i; })
      .sort(function(a,b){ return a.localeCompare(b, 'fr'); });
  }
  function countBy(key, value){
    return value === 'all' ? window.GE_PRODUCTS.length : window.GE_PRODUCTS.filter(function(p){ return p[key] === value; }).length;
  }
  function searchableText(p){
    return normalize([
      p.title,
      p.category,
      p.brand,
      p.tag,
      p.delivery,
      p.desc,
      (p.features || []).join(' '),
      Object.keys(p.specs || {}).join(' '),
      Object.values(p.specs || {}).join(' ')
    ].join(' '));
  }
  function brandLogoHtml(brand, context){
    var logo = BRAND_LOGOS[brand];
    var cls = context === 'filter' ? 'ge-filter-brand-logo' : (context === 'details' ? 'ge-details-brand-logo' : 'ge-card-brand-logo');
    if(!logo){
      return '<span class="'+cls+' ge-brand-logo-fallback">'+brand+'</span>';
    }
    return '<span class="'+cls+' ge-brand-logo--'+normalize(brand)+'" title="'+escapeAttr(brand)+'"><img src="'+logo+'" alt="'+escapeAttr(brand)+' logo" loading="lazy"><em>'+brand+'</em></span>';
  }
  function productCard(p){
    return '<a class="ge-shop-card" href="products-details.html?id='+encodeURIComponent(p.id)+'" data-category="'+escapeAttr(p.category)+'" data-brand="'+escapeAttr(p.brand)+'" data-title="'+escapeAttr(normalize(p.title))+'">' +
      '<div class="ge-shop-card__media"><img src="'+p.image+'" alt="'+escapeAttr(p.title)+'" loading="lazy"></div>'+
      '<div class="ge-shop-card__body">'+
        '<div class="ge-card-info"><span class="ge-card-category">'+p.category+'</span>'+brandLogoHtml(p.brand, 'card')+'</div>'+
        '<h3>'+p.title+'</h3><p>'+p.desc+'</p>'+
        '<div class="ge-shop-card__store"><strong>Voir détails <span>→</span></strong></div>'+ 
      '</div></a>';
  }
  function emptyState(){
    return '<div class="ge-products-empty"><b>Aucun produit trouvé</b><span>Essayez une autre recherche, une autre marque ou une autre catégorie.</span></div>';
  }
  function buildFilterButtons(container, key, allLabel){
    if(!container) return;
    var values = uniqueValues(key);
    var html = '<button class="ge-products-chip is-active" type="button" data-filter="'+key+'" data-value="all"><span>'+allLabel+'</span><b>'+countBy(key, 'all')+'</b></button>';
    html += values.map(function(value){
      var label = key === 'brand' ? brandLogoHtml(value, 'filter') : '<span>'+value+'</span>';
      return '<button class="ge-products-chip '+(key === 'brand' ? 'ge-brand-filter-chip' : '')+'" type="button" data-filter="'+key+'" data-value="'+escapeAttr(value)+'" aria-label="Marque '+escapeAttr(value)+'">'+label+'<b>'+countBy(key, value)+'</b></button>';
    }).join('');
    container.innerHTML = html;
  }
  function renderListing(){
    var grid = $('#productsGrid');
    if(!grid) return;
    var search = $('#productSearch');
    var searchButton = $('#productSearchButton');
    var meta = $('#productsResultMeta');
    var clear = $('#clearProductFilters');
    var filters = { category: 'all', brand: 'all' };

    buildFilterButtons($('#categoryFilters'), 'category', 'Toutes les catégories');
    buildFilterButtons($('#brandFilters'), 'brand', 'Toutes les marques');

    function setActiveButton(type, value){
      $all('.ge-products-chip[data-filter="'+type+'"]').forEach(function(btn){
        btn.classList.toggle('is-active', (btn.dataset.value || 'all') === value);
      });
    }
    function update(){
      var terms = normalize(search ? search.value : '').split(' ').filter(Boolean);
      var shown = window.GE_PRODUCTS.filter(function(p){
        var okCat = filters.category === 'all' || p.category === filters.category;
        var okBrand = filters.brand === 'all' || p.brand === filters.brand;
        var haystack = searchableText(p);
        var okSearch = !terms.length || terms.every(function(term){ return haystack.indexOf(term) !== -1; });
        return okCat && okBrand && okSearch;
      });
      grid.innerHTML = shown.length ? shown.map(productCard).join('') : emptyState();
      if(meta){
        meta.textContent = shown.length + ' résultat' + (shown.length > 1 ? 's' : '') + ' trouvé' + (shown.length > 1 ? 's' : '');
      }
    }
    $all('.ge-products-chip').forEach(function(chip){
      chip.addEventListener('click', function(){
        var type = chip.dataset.filter;
        var value = chip.dataset.value || 'all';
        filters[type] = value;
        setActiveButton(type, value);
        update();
      });
    });
    if(search){
      search.addEventListener('input', update);
      search.addEventListener('keydown', function(e){ if(e.key === 'Enter'){ e.preventDefault(); update(); } });
    }
    if(searchButton){ searchButton.addEventListener('click', update); }
    if(clear){
      clear.addEventListener('click', function(){
        filters.category = 'all';
        filters.brand = 'all';
        if(search) search.value = '';
        setActiveButton('category', 'all');
        setActiveButton('brand', 'all');
        update();
      });
    }
    update();
  }
  function renderDetails(){
    var mount = $('#productDetails'); if(!mount) return;
    var params = new URLSearchParams(window.location.search);
    var id = params.get('id') || 'split-mural-inverter';
    var p = window.GE_PRODUCTS.find(function(x){ return x.id === id; }) || window.GE_PRODUCTS[0];
    document.title = p.title + ' - Global Energie Events';
    var specs = Object.keys(p.specs).map(function(k){ return '<li><span>'+k+'</span><strong>'+p.specs[k]+'</strong></li>'; }).join('');
    var features = p.features.map(function(f){ return '<li>'+f+'</li>'; }).join('');
    mount.innerHTML = '<section class="ge-details-hero">'+
      '<div class="ge-details-gallery">'+
        '<div class="ge-details-side"><div class="ge-details-thumbs">'+p.gallery.map(function(src,i){ return '<button class="'+(i===0?'is-active':'')+'" data-src="'+src+'"><img src="'+src+'" alt="'+p.title+' miniature '+(i+1)+'"></button>'; }).join('')+'</div><a class="ge-details-situation" href="products.html">Voir en situation</a></div>'+ 
        '<div class="ge-details-main"><span class="ge-zoom-hint">Zoom</span><img id="mainProductImage" src="'+p.gallery[0]+'" alt="'+p.title+'"></div>'+ 
      '</div>'+ 
      '<div class="ge-details-info"><div class="ge-details-kicker"><span class="ge-details-category-pill">'+p.category+'</span>'+brandLogoHtml(p.brand, 'details')+'</div><h1>'+p.title+'</h1>'+ 
      '<div class="ge-details-rating"><em>'+p.delivery+'</em><em class="ge-details-brand-em">Marque '+p.brand+'</em></div>'+ 
      '<p class="ge-details-desc">'+p.desc+'</p>'+ 
      '<ul class="ge-details-features">'+features+'</ul>'+ 
      '<ul class="ge-details-specs">'+specs+'</ul>'+ 
      '<div class="ge-details-actions"><a class="ge-details-contact" href="contact.html">Contactez-nous pour plus d’informations <span>→</span></a><a class="ge-details-whatsapp" href="https://wa.me/212661717581" rel="noopener" target="_blank">Demander sur WhatsApp <span>→</span></a></div>'+ 
      '</div></section>'+ 
      '<section class="ge-related"><div class="ge-related-head"><h2>Autres produits</h2><a href="products.html">Voir tous les produits →</a></div><div class="ge-related-grid">'+
      window.GE_PRODUCTS.filter(function(x){ return x.id !== p.id; }).slice(0,4).map(productCard).join('') + '</div></section>'+ 
      '<div class="ge-lightbox" id="geLightbox"><button type="button" aria-label="Fermer">×</button><img alt="Agrandissement produit"></div>';
    var main = $('#mainProductImage');
    var thumbButtons = $all('.ge-details-thumbs button');
    var frame = $('.ge-details-main');
    var lightbox = $('#geLightbox'); var lightImg = $('#geLightbox img');
    var currentIndex = 0;
    var galleryTimer = null;

    function setActiveImage(index){
      if(!p.gallery || !p.gallery.length) return;
      currentIndex = (index + p.gallery.length) % p.gallery.length;
      var activeSrc = p.gallery[currentIndex];
      main.src = activeSrc;
      thumbButtons.forEach(function(b, i){ b.classList.toggle('is-active', i === currentIndex); });
      if(lightbox.classList.contains('is-open')){ lightImg.src = activeSrc; }
    }
    function stopAutoGallery(){
      if(galleryTimer){ clearInterval(galleryTimer); galleryTimer = null; }
    }
    function startAutoGallery(){
      stopAutoGallery();
      if(!p.gallery || p.gallery.length < 2) return;
      galleryTimer = setInterval(function(){ setActiveImage(currentIndex + 1); }, 2800);
    }

    thumbButtons.forEach(function(btn, index){
      btn.addEventListener('click', function(){
        setActiveImage(index);
        startAutoGallery();
      });
    });
    setActiveImage(0);
    startAutoGallery();

    frame.addEventListener('mousemove', function(e){ var r = frame.getBoundingClientRect(); var x = ((e.clientX-r.left)/r.width)*100; var y=((e.clientY-r.top)/r.height)*100; main.style.transformOrigin = x+'% '+y+'%'; main.classList.add('is-zooming'); });
    frame.addEventListener('mouseenter', stopAutoGallery);
    frame.addEventListener('mouseleave', function(){ main.classList.remove('is-zooming'); main.style.transformOrigin='center'; startAutoGallery(); });
    frame.addEventListener('click', function(){ lightImg.src = main.src; lightbox.classList.add('is-open'); stopAutoGallery(); });
    $('#geLightbox button').addEventListener('click', function(){ lightbox.classList.remove('is-open'); startAutoGallery(); });
    lightbox.addEventListener('click', function(e){ if(e.target === lightbox) { lightbox.classList.remove('is-open'); startAutoGallery(); } });
    document.addEventListener('visibilitychange', function(){ if(document.hidden){ stopAutoGallery(); } else { startAutoGallery(); } });
  }
  document.addEventListener('DOMContentLoaded', function(){ renderListing(); renderDetails(); });
})();


(function(){
  function initBenefitsSlider(){
    var slider = document.querySelector('[data-benefits-slider]');
    if(!slider) return;
    var track = slider.querySelector('.ge-benefits-track');
    var slides = Array.prototype.slice.call(slider.querySelectorAll('.ge-benefit-slide'));
    var dots = Array.prototype.slice.call(slider.querySelectorAll('[data-benefit-dot]'));
    var prev = slider.querySelector('[data-benefit-prev]');
    var next = slider.querySelector('[data-benefit-next]');
    var index = 0;
    var timer = null;
    function go(i){
      if(!slides.length) return;
      index = (i + slides.length) % slides.length;
      track.style.transform = 'translateX(-' + (index * 100) + '%)';
      slides.forEach(function(slide, n){ slide.classList.toggle('is-active', n === index); });
      dots.forEach(function(dot, n){ dot.classList.toggle('is-active', n === index); });
    }
    function start(){ stop(); timer = setInterval(function(){ go(index + 1); }, 4300); }
    function stop(){ if(timer){ clearInterval(timer); timer = null; } }
    if(prev){ prev.addEventListener('click', function(){ go(index - 1); start(); }); }
    if(next){ next.addEventListener('click', function(){ go(index + 1); start(); }); }
    dots.forEach(function(dot){ dot.addEventListener('click', function(){ go(parseInt(dot.getAttribute('data-benefit-dot'), 10) || 0); start(); }); });
    slider.addEventListener('mouseenter', stop);
    slider.addEventListener('mouseleave', start);
    go(0);
    start();
  }
  document.addEventListener('DOMContentLoaded', initBenefitsSlider);
})();
